package day10_ifStatements;

public class multiBranch_If {
    public static void main(String[] args) {

        int number = -58;

        if(number > 0){
            System.out.println("positive");
        }else if (number < 0){
            System.out.println("negative");
        }else{
            System.out.println("zero");
        }



    }


}
