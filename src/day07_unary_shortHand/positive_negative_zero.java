package day07_unary_shortHand;

public class positive_negative_zero {

    public static void main(String[] args) {


        int num1 = 0;

        boolean pos1 = num1 > 0;
        boolean neg1 = num1 < 0;
        boolean zero1 = num1 ==0;
        System.out.println(num1 +  " Is positive? " + pos1);
        System.out.println(num1 + " Is negative? " + neg1);
        System.out.println(num1 + " Is  Zero? " + zero1);
        System.out.println("");

    }
}
