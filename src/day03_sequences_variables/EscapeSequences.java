package day03_sequences_variables;

public class EscapeSequences {

    public static void main(String[] args){

        System.out.println("Hello Everyone, \n\tMy name is Lisa");
        System.out.println("\tI am in Virginia\n\n\n");

        System.out.println("I love programming");
        System.out.println(("\\\\")); //this prints me two backslash
        System.out.println("//\n"); //this prints me two front slash

        System.out.println("My name is \'Lisa\'"); // 'Lisa'
        System.out.println("My name is 'Lisa'\n");

        System.out.println("My favorite TV-series: \"Game of Thrones\"");



    }

}
