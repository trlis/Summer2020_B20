package day12_switch_scaner;

public class Switch {
    public static void main(String[] args) {

        switch(2){

            case 2:
                System.out.println("Two");
                break;

            case 1:
                System.out.println("One");
                break;


            default:
                System.out.println("Invalid case");
                break;


        }
    }
}
